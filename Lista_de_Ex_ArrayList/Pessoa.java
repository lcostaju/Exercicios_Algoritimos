package Lista_de_Ex_ArrayList;

public class Pessoa {
    
    private String nome;
    private Integer num;
    
    public Pessoa(String nome, Integer num) {
        this.nome = nome;
        this.num = num;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public Integer getNum() {
        return num;
    }
    public void setNum(Integer num) {
        this.num = num;
    }

    
}
